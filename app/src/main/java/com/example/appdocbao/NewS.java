package com.example.appdocbao;

public class NewS {
    private String img;
    private String title;
    private String link;

    public NewS(String img, String title, String link) {
        this.img = img;
        this.title = title;
        this.link = link;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
